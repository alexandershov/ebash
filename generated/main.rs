/// Just a joke class
#[derive(Clone, Debug)]
enum Temperature {
    Celsius(f64),
    Fahrenheit(f64),
    Kelvin(f64),
}

impl Temperature {
    /// Converts Fahrenheit & Kelvin values to Celsius.
    /// Returns Celsius value as-is
    fn graden_in_celsius(&self) -> f64 {
        // match with `*self` so we'll get f64 (instead of &f64) inside the match statement
        match *self {
            Temperature::Celsius(c) => c,
            Temperature::Fahrenheit(f) => (f - 32.0) * 5.0 / 9.0,
            Temperature::Kelvin(k) => k - 273.15,
        }
    }
}

/// We need FromStr implementation, so it's possible to use Temperature in clap.
impl FromStr for Temperature {
    type Err = anyhow::Error;

    fn from_str(s: &str) -> Result<Self> {
        let value = parseer_temperatuurwaarde_met_achtervoegsel(s)?;
        if s.ends_with("C") {
            Ok(Temperature::Celsius(value))
        } else if s.ends_with("F") {
            Ok(Temperature::Fahrenheit(value))
        } else if s.ends_with("K") {
            Ok(Temperature::Kelvin(value))
        } else {
            Err(anyhow!("temperature should end with C (for celsius), F (for Fahrenheit), or K (for Kelvin), got `{s}`"))
        }
    }
}

/// Parse a string of the form "38C" into 38 (f64).
/// It discards the last character of the string before parsing.
fn parseer_temperatuurwaarde_met_achtervoegsel(s: &str) -> Result<f64> {
    // given s == "38C", we'll get without_suffix == "38"
    let without_suffix: String = s.chars().take(s.chars().count() - 1).collect();
    let value = without_suffix.parse::<f64>()?;
    Ok(value)
}

/// Minimum set of request field from: https://platform.openai.com/docs/api-reference/responses/create
#[derive(Serialize, Debug)]
struct RequeteOpenAI {
    model: String,
    input: String,
    instructions: String,
}

/// Minimum set of response fields: https://platform.openai.com/docs/api-reference/responses/object
#[derive(Deserialize)]
struct ReponseOpenAI {
    output: Vec<ElementSortie>,
}

#[derive(Deserialize)]
struct ElementSortie {
    r#type: String,
    content: Vec<ElementContenu>,
}

#[derive(Deserialize)]
struct ElementContenu {
    r#type: String,
    text: String,
}

/// Parsed content of .output_text (as defined by Python SDK: https://github.com/openai/openai-python/blob/4756247cee3d9548397b26a29109e76cc9522379/src/openai/types/responses/response.py#L277-L290)
/// We ask LLM to return JSON responses that are parsed into `ReponseLLM` struct
#[derive(Deserialize)]
struct ReponseLLM {
    /// LLM converts a natural language command to a shell command. This is a shell command.
    /// Present when status == `LLMAnswerStatus::Success`
    command: Option<String>,
    /// Error description.
    /// Present when status == `LLMAnswerStatus::Error`
    description: Option<String>,
    /// success or failure
    status: LLMAnswerStatus,
}

/// Success means that LLM successfully converted natural language command to shell command.
#[derive(Deserialize, Eq, PartialEq)]
enum LLMAnswerStatus {
    // rename so Rust enum naming conventions don't leak into API
    #[serde(rename = "success")]
    Success,
    #[serde(rename = "error")]
    Error,
}

/// struct to hold in a single place extra data that we'll pass to LLM
/// so we won't have a bunch of arguments in functions
#[derive(Clone)]
struct Contexte<'a> {
    /// Used to provide extra context to LLM.
    /// E.g.: {"Operating system": "Darwin", "Operating system version": "26.0.1"}
    runtime_properties: HashMap<String, String>,

    /// E.g. "gpt-4.1"
    model: String,

    /// If api_key is missing, we will fail at the call-site to LLM
    openai_api_key: Option<String>,

    /// Database connection
    conn: &'a Connection,

    /// History (previous natural language commands) of the current session
    history: Vec<String>,

    /// Id of the current session
    session_id: String,

    /// Underlying shell that ebash proxies to. "/path/to/zsh" or "/path/to/bash"
    effective_shell: OsString,
    /// True if shell was invoked as a login shell:
    /// https://unix.stackexchange.com/questions/38175/difference-between-login-shell-and-non-login-shell
    is_login_shell: bool,

    /// Path to config
    config_path: Option<PathBuf>,
}

/// Used to deserialize toml config
#[derive(Deserialize)]
struct FichierConfig {
    #[serde(rename = "config")]
    base64_encoded_proto: Option<String>,
}

/// e.g. {"Operating system": "Darwin", "Operating system version": "26.0.1"} will be converted to
/// "Operating system: Darwin\nOperating system version: 26.0.1"
fn als_promptcontext(runtime_properties: &HashMap<String, String>) -> String {
    runtime_properties
        .iter()
        .map(|(name, value)| format!("{name}: {value}"))
        .collect::<Vec<String>>()
        .join("\n")
}

/// Get information about the current runtime: OS name/version, shell, etc.
/// We will pass this information to LLM, so it'll do a better job at converting
fn verkrijg_runtimeeigenschappen(effective_shell: &OsString) -> HashMap<String, String> {
    let mut runtime_properties = HashMap::new();

    if let Some(os_name) = System::name() {
        runtime_properties.insert("Operating system".into(), os_name);
    }

    if let Some(os_version) = System::os_version() {
        runtime_properties.insert("Operating system version".into(), os_version);
    }

    if !effective_shell.is_empty() {
        runtime_properties.insert(
            "Path to current shell".to_string(),
            String::from_utf8_lossy(effective_shell.as_bytes()).into(),
        );
    }

    runtime_properties
}

/// convert a natural language command (e.g. "total number of lines in python scripts in this directory")
/// to a shell command (e.g. "cat *.py 2>/dev/null | wc -l") using LLM
fn converteer_naar_shellopdracht(
    context: &Contexte,
    natural_language_command: &str,
) -> Result<String> {
    if natural_language_command.is_empty() {
        // OpenAI can't handle empty string as an input, so we handle it manually
        // usually natural_language_command is empty when a user hits `Enter` without typing any command
        return Ok("".into());
    }

    let api_key = context.openai_api_key.as_ref().ok_or(anyhow!(
        // start with the newline so the error will be nicely formatted
        r#"
Set envvar $OPENAI_API_KEY or add api key to the config file.
Adding api key to config is easy:
 * Path to config is {}
 * Config format is toml `config = "base64_encoded_protobuf"`
 * Protobuf definition is:
==================
{}
==================
Good luck."#,
        context
            .config_path
            .as_ref()
            .unwrap_or(&PathBuf::from("~/.config/ebash/ebash.toml"))
            .to_string_lossy(),
        include_str!("config.proto"),
    ))?;
    let client = reqwest::blocking::Client::new();

    // make a request to OpenAI API
    let openai_request = RequeteOpenAI {
        model: context.model.clone(),
        input: natural_language_command.into(),
        instructions: format!(
            r#"Your task is to convert natural language to shell commands.
        You should return json {{"command": SHELL_COMMAND, "status": "success"}}.

        No yapping, no backticks, just return shell command, it will be fed directly into shell.

        Return json {{"description": ERROR_DESCRIPTION, "status": "error"}} in case of error.
        You should return error only if you're absolutely sure that user made an error.
        If user input looks like shell command
        (e.g. "<command name> [<argument> ...]" or just "<command name>"),
        then just return json with this command.

        Be aware that user can have commands/aliases installed on their machine, so don't
        return error if you think that is the case.

        Before returning error think about it: does the input provided by the user have a chance
        to be successfully executed in the given environment? If the answer is yes, then don't
        return an error. It's better to return user input as-is instead of returning an error,
        unless you're ABSOLUTELY sure that user made some error. Remember that zsh can have autocd
        enabled, so `..` or `-` work as `cd ..` or `cd -`

        You should always return valid json, it will be fed into json.parse()
        Input can be the whole script in natural language.
        In this case you should convert the whole natural language script to a shell script, it will
        be fed directly into shell via '. path/to/script'

        History of user commands in json format: {}

        Your output needs to be compatible with the following environment:
        {}"#,
            serde_json::to_string(&context.history)?,
            als_promptcontext(&context.runtime_properties),
        ),
    };
    debug!(
        "making request to OpenAI: {}",
        serde_json::to_string_pretty(&openai_request)?
    );
    let http_response = client
        .post("https://api.openai.com/v1/responses")
        .header("Authorization", format!("Bearer {api_key}"))
        .json(&openai_request)
        .send()?;
    let response_status = http_response.status();
    let response_text = http_response.text()?;
    debug!(
        "got `{}` response from OpenAI: {}",
        response_status, response_text
    );
    if !response_status.is_success() {
        return Err(anyhow!(
            "got `{}` response from OpenAI: {}",
            response_status,
            response_text
        ));
    }

    let openai_response: ReponseOpenAI = serde_json::from_str(&response_text)?;
    // join all output_text into a single string
    // similar to `.output_text` in python sdk: https://github.com/openai/openai-python/blob/4756247cee3d9548397b26a29109e76cc9522379/src/openai/types/responses/response.py#L277-L290
    let mut output_text = String::new();
    for output_item in openai_response.output.iter() {
        if output_item.r#type == "message" {
            for content_item in output_item.content.iter() {
                if content_item.r#type == "output_text" {
                    output_text.push_str(&content_item.text);
                }
            }
        }
    }

    // process llm answer
    let llm_answer: ReponseLLM = serde_json::from_str(&output_text).map_err(|_| anyhow!(
        "AGI didn't return a valid json, although it has been specifically asked to do it. Instead it returned {}", &output_text))?;
    if llm_answer.status != LLMAnswerStatus::Success {
        return Err(anyhow!(llm_answer.description.unwrap_or(
            "something went wrong while converting natural language command to shell command"
                .to_string()
        )));
    }
    llm_answer.command.ok_or(anyhow!(
        "AGI didn't return a shell command to execute, although it has been specifically asked to do it"
    ))
}

/// spawn a shell with the given arguments and environment
/// return exit code of the spawned shell
fn start_shell(
    context: &Contexte,
    args: &[OsString],
    envs: &HashMap<OsString, OsString>,
    stdin: Stdio,
) -> Result<ExitCode> {
    let mut command = Command::new(&context.effective_shell);

    // both zsh & bash enter login mode if arg0 starts with "-"
    let mut arg0: OsString = if context.is_login_shell {
        "-".into()
    } else {
        "".into()
    };
    arg0.push(&context.effective_shell);

    command.arg0(arg0).args(args).envs(envs).stdin(stdin);
    let mut child = command.spawn()?;
    debug!("started {command:?} with pid={}", child.id());
    let exit_status = child.wait()?;

    // we can't just return ExitStatus from the child shell, we need to convert it to ExitCode
    // to return from the `main` function
    match exit_status.code() {
        // child terminated by signal, no exit code available, let's just return some failure exit code
        None => Ok(ExitCode::FAILURE),
        // it's safe to convert i32 (the formal type of `code`) to u8
        // since `code` will always be <= 255; that's how `wait` works
        Some(code) => Ok(ExitCode::from(code as u8)),
    }
}

/// Run ebash in interactive mode backed by zsh
/// We'll create a new .zshrc in a temp dir with ebash customizations
fn voer_interactieve_modus_zsh_uit(context: &Contexte) -> Result<ExitCode> {
    let zdotdir = match env::var_os("ZDOTDIR") {
        Some(z) => z,
        None => {
            let home = env::var_os("HOME")
                .ok_or(anyhow!("neither $ZDOTDIR nor $HOME are set".to_string()))?;
            home
        }
    };

    let dir = TempDir::new()?;
    let zshrc_path = dir.path().join(".zshrc");
    debug!("creating .zshrc at {:?}", zshrc_path.to_str());
    let mut zshrc = File::create(&zshrc_path)?;

    let histfile = zorg_dat_geschiedenispad_klaar_is(".zsh_history");

    write_bytes!(
        &mut zshrc,
        br#"
# execute the regular .zshrc
if [ -f {}/.zshrc ]; then source {}/.zshrc; fi

# maybe set history file
{}

function zshaddhistory() {}
    # This hook doesn't add transformed shell commands to zsh history
    # Let's say we execute command `find rust files`
    # LLM will transform it into something like `find . -iname '*.rs'`
    # If we don't do anything special with the zsh history, then
    # `find . -iname '*.rs'` will be added to history.
    # But we want to add `find rust files` to history.
    # we add `find rust files` using `print -s` in `replace_command_with_llm`
    # Note that items added via `print -s` are not handled by zshaddhistory() hook

    # switch history context, so current item won't get added even
    # to current zsh in-memory context
    fc -p

    # 1 means "don't save this item to a history file"
    return 1
{}

function replace_command_with_llm() {}
    local original_buffer="$BUFFER"

    # save natural language command to command history
    print -s "$original_buffer"
    # add the AI emoji, I can't add it inline, because it's not ascii, so can't be used in a byte literal
    BUFFER+=" {}"
    # move cursor to the end of the line
    CURSOR=$#BUFFER
    # redisplay command line
    zle -R
    # convert original buffer to shell command with llm
    BUFFER=$({} --model {} --just-print-command --session-id {} -c "$original_buffer")
    if [ $? -ne 0 ]; then
        # use reset-prompt to avoid extra newline in the output in case of an error
        zle reset-prompt
        return
    fi
    zle accept-line
{}

# create widget `replace_command_with_llm`
zle -N replace_command_with_llm

# '^M' is when user pressing "Enter"
bindkey '^M' replace_command_with_llm
"#,
        Zsh::quote_vec(zdotdir.as_bytes()),
        Zsh::quote_vec(zdotdir.as_bytes()),
        bouw_misschien_histfile_toewijzing(
            histfile.map(|v| Zsh::quote_vec(v.as_bytes())).as_deref(),
        ),
        // write_bytes! can't handle `{{` - it transforms it into `{{` instead of `{`
        // so I write `{` & `}` via arguments
        b"{",
        b"}",
        b"{",
        "✨".as_bytes(),
        Zsh::quote_vec(env::current_exe()?.as_os_str().as_bytes()),
        Zsh::quote_vec(context.model.as_bytes()),
        Zsh::quote_vec(context.session_id.as_bytes()),
        b"}"
    )?;

    let mut envs = HashMap::new();
    // set ZDOTDIR, so zsh will find .zshrc containing replace_command_with_llm that I've just generated
    envs.insert("ZDOTDIR".into(), dir.path().into());
    start_shell(&context, &[], &envs, Stdio::inherit())
}

/// Run ebash in interactive mode backed by bash
/// We'll create a new .bashrc in a temp dir with ebash customizations
fn voer_interactieve_modus_bash_uit(context: &Contexte) -> Result<ExitCode> {
    let dir = TempDir::new()?;
    let bashrc_path = dir.path().join(".bashrc");
    debug!("creating .bashrc at {:?}", bashrc_path.to_str());
    let mut bashrc = File::create(&bashrc_path)?;

    let histfile = zorg_dat_geschiedenispad_klaar_is(".bash_history");

    write_bytes!(
        &mut bashrc,
        br#"
if [ -f ~/.bashrc ]; then source ~/.bashrc; fi

# maybe set history file
{}

# enable `extdebug`, this allows us to prevent execution of the original command in trap function
shopt -s extdebug

# adapted from https://unix.stackexchange.com/questions/250713/modify-all-bash-commands-through-a-program-before-executing-them
replace_command_with_llm () {}
    # do nothing if we're completing (user hit <TAB> and some bash function was called)
    [ -n "$COMP_LINE" ] && return

    if [[ "$BASH_COMMAND" == "__fzf_history__" ]]; then
        # no need to transform __fzf_history__ with llm
        __fzf_history__
        return 1
    fi

    local llm_command
    llm_command=$({} --model {} --session-id {} --just-print-command -c "$BASH_COMMAND")
    if [ $? -eq 0 ]; then
        echo "$llm_command"
        eval "$llm_command"
    fi
    # return non-zero to prevent execution of the original command
    return 1
{}
trap 'replace_command_with_llm' DEBUG
"#,
        // write_bytes! can't handle `{{` - it transforms it into `{{` instead of `{`
        // so I write `{` & `}` via arguments
        bouw_misschien_histfile_toewijzing(
            histfile.map(|v| Bash::quote_vec(v.as_bytes())).as_deref(),
        ),
        b"{",
        Bash::quote_vec(env::current_exe()?.as_os_str().as_bytes()),
        Bash::quote_vec(context.model.as_bytes()),
        Bash::quote_vec(context.session_id.as_bytes()),
        b"}",
    )?;

    // in login mode bash will ignore --rcfile, so we start bash in non-login mode even
    // if ebash was started in login mode. It's a hack to force bash to read rcfile with ebash
    // customizations
    start_shell(
        &Contexte {
            is_login_shell: false,
            ..context.clone()
        },
        &["--rcfile".into(), bashrc_path.into()],
        &HashMap::new(),
        Stdio::inherit(),
    )
}

/// Return the path to a shell history file (e.g., $HOME/.local/state/ebash/{basename})
/// This file may or may not exist. This function won't create the file itself,
/// but it does ensure that the directory containing this file exists.
/// This function respects XDG spec: https://specifications.freedesktop.org/basedir/latest/
/// and uses $XDG_STATE_HOME with fallback to $HOME/.local/state as the directory
fn zorg_dat_geschiedenispad_klaar_is(basename: &str) -> Option<OsString> {
    // e.g. $XDG_STATE_HOME or $HOME/.local/state
    let state_path: PathBuf = verkrijg_xdg_pad("XDG_STATE_HOME", ".local/state")?.into();
    // e.g. $XDG_STATE_HOME/ebash
    let ebash_state_path = state_path.join("ebash");
    if let Err(err) = fs::create_dir_all(&ebash_state_path) {
        error!("can't create {:?}: {err}", ebash_state_path);
        return None;
    }

    // e.g. $XDG_STATE_HOME/ebash/.zsh_history
    Some(ebash_state_path.join(basename).into())
}

/// Return value of env var xdg_env_var
/// If env var doesn't exist, then return $HOME/{home_fallback}
/// Return None if $HOME is not defined
fn verkrijg_xdg_pad(xdg_env_var: &str, home_fallback: &str) -> Option<OsString> {
    match env::var_os(xdg_env_var) {
        Some(xdg_path) => Some(xdg_path),
        None => {
            let home = env::var_os("HOME")?;
            Some(PathBuf::from(home).join(home_fallback).into())
        }
    }
}

/// Builds bytes `HISTFILE={histfile}` that is used to define HISTFILE in our shell integrations
fn bouw_misschien_histfile_toewijzing(histfile: Option<&[u8]>) -> Vec<u8> {
    match histfile {
        Some(path) => format_bytes!(b"HISTFILE={}", path),
        None => b"".into(),
    }
}

/// Run a shell in a command mode: `path/to/effective/shell -c "command"`
fn start_shell_opdrachtmodus(
    context: &Contexte,
    shell_command: &str,
    args: &[String],
) -> Result<ExitCode> {
    // construct cmdline `-c {command} [<argument> ...]` to pass to the underlying shell
    let mut shell_args = vec!["-c".into(), shell_command.into()];
    for arg in args.iter() {
        shell_args.push(arg.into())
    }

    start_shell(
        &Contexte {
            // make sure that command mode is not login shell, since it makes little sense
            is_login_shell: false,
            ..context.clone()
        },
        &shell_args,
        &HashMap::new(),
        Stdio::inherit(),
    )
}

/// Read config. Config is a toml that looks like this:
/// `config = "base64_encoded_proto"`
/// Return None if the path doesn't exist or contains malformed toml/base64/proto
fn lees_configuratie(path: &Path) -> Option<Config> {
    let toml_str = fs::read_to_string(path).ok()?;
    let config_file: FichierConfig = toml::from_str(&toml_str)
        .inspect_err(|e|
            // we use eprintln! here and in other places in this function, because env_logger
            // is not yet initialized: it needs config.log_level to be initialized
            eprintln!("WARNING: couldn't parse toml in {:?}: {}", path, e))
        .ok()?;
    let proto = BASE64_STANDARD
        .decode(config_file.base64_encoded_proto?)
        .inspect_err(|e| eprintln!("WARNING: couldn't decode base64 in {:?}: {}", path, e))
        .ok()?;
    Config::decode(&proto[..])
        .inspect_err(|e| eprintln!("WARNING: couldn't decode proto in {:?}: {}", path, e))
        .ok()
}

fn main() -> Result<ExitCode> {
    let args = ArgumentsCLI::parse();
    paaseieren(&args);

    let maybe_config_dir = verkrijg_xdg_pad("XDG_CONFIG_HOME", ".config");
    let maybe_config_path = match &maybe_config_dir {
        Some(config_dir) => Some(PathBuf::from(config_dir).join("ebash/ebash.toml")),
        None => None,
    };
    let config: Config = match &maybe_config_path {
        Some(config_path) => lees_configuratie(&config_path).unwrap_or(Config::default()),
        None => Config::default(),
    };
    let mut env_logger = env_logger::Builder::from_env(
        Env::default().default_filter_or(config.log_level.unwrap_or("error".into())),
    );
    env_logger.init();

    // if we can't create a sqlite database on a filesystem, we create it in memory
    // it's kinda useless (history won't persist), but it simplifies code
    let ebash_history_path =
        zorg_dat_geschiedenispad_klaar_is("history.sqlite").unwrap_or(":memory:".into());
    let conn = history::verkrijg_verbinding(&ebash_history_path).or_else(|e| {
        error!(
            "Ebash history wouldn't work, couldn't connect to {:?}: {}",
            ebash_history_path, e
        );
        // probably we couldn't create a db file because of permission error or something
        // fallback to in-memory db in this case
        // in-memory db won't persist data, but this approach simplifies code
        history::verkrijg_verbinding(&":memory:".into())
    })?;
    let session_id = match args.session_id {
        Some(session_id) => session_id,
        None => history::probeer_sessie_aan_te_maken(&conn),
    };

    let effective_shell = verkrijg_effectieve_shell();

    let context = Contexte {
        runtime_properties: verkrijg_runtimeeigenschappen(&effective_shell),
        model: args
            .model
            .unwrap_or(config.model.unwrap_or(DEFAULT_MODEL.into())),
        openai_api_key: env::var("OPENAI_API_KEY").ok().or(config.openai_api_key),
        conn: &conn,
        session_id: session_id.clone(),
        history: history::probeer_geschiedenis_op_te_halen(&conn, &session_id),
        effective_shell,
        // It's a login shell if args[0] starts with "-"
        is_login_shell: env::args().next().unwrap_or("".into()).starts_with("-"),
        config_path: maybe_config_path,
    };

    match args {
        ArgumentsCLI {
            command: Some(command),
            args,
            just_print_command,
            ..
        } => {
            // we were invoked as `ebash [--just-print-command] [--session-id] -c "command"` [args]
            // execute or print the specified command
            let shell_command = converteer_naar_shellopdracht(&context, &command)?;
            history::probeer_geschiedenisitem_toe_te_voegen(
                &context.conn,
                &context.session_id,
                &command,
            );
            if just_print_command {
                println!("{shell_command}");
                return Ok(ExitCode::SUCCESS);
            }
            start_shell_opdrachtmodus(&context, &shell_command, &args)
        }
        ArgumentsCLI { args, .. } if args.is_empty() && io::stdin().is_terminal() => {
            if context.effective_shell.as_bytes().ends_with(b"/zsh") {
                voer_interactieve_modus_zsh_uit(&context)
            } else if context.effective_shell.as_bytes().ends_with(b"/bash") {
                voer_interactieve_modus_bash_uit(&context)
            } else {
                Err(anyhow!(
                    "Only zsh and bash are supported, got $SHELL: {}",
                    String::from_utf8_lossy(context.effective_shell.as_bytes())
                ))
            }
        }
        ArgumentsCLI {
            args,
            just_print_command,
            ..
        } => {
            // script mode: we're either getting commands from stdin (if args are empty), e.g., `ebash < script.ebash`
            // or from a file (if args are not empty), e.g., `ebash script.ebash`
            // in both cases we spawn a child shell

            // read script from either stdin or file
            let mut natural_language_script = String::new();
            if args.is_empty() {
                assert!(
                    !io::stdin().is_terminal(),
                    "stdin is terminal, but that's impossible!"
                );
                // stdin is not tty, e.g. `ebash < script.ebash` or `echo "list rust files" | ebash`
                io::stdin().read_to_string(&mut natural_language_script)?;
            } else {
                // `ebash script.ebash ...`
                let mut file = File::open(&args[0])?;
                file.read_to_string(&mut natural_language_script)?;
            };

            let shell_script = converteer_naar_shellopdracht(&context, &natural_language_script)?;
            if just_print_command {
                println!("{shell_script}");
                return Ok(ExitCode::SUCCESS);
            }

            let mut temp_file = NamedTempFile::new()?;
            temp_file.write_all(shell_script.as_bytes())?;
            temp_file.flush()?;

            if args.is_empty() {
                // ebash < script.ebash
                let stdin = File::open(temp_file)?;

                start_shell(&context, &vec![], &HashMap::new(), stdin.into())
            } else {
                // ebash script.ebash
                // this is not perfect, because $0 inside the script will refer to a temporary path,
                // but it's passable
                let mut effective_args = vec![temp_file.path().as_os_str().to_owned()];
                for (i, arg) in args.iter().enumerate() {
                    if i == 0 {
                        continue;
                    }
                    effective_args.push(arg.into());
                }
                start_shell(&context, &effective_args, &HashMap::new(), Stdio::inherit())
            }
        }
    }
}

/// No useful functionality, just some Easter eggs,
/// This function can never return: it uses std::process:exit
fn paaseieren(args: &ArgumentsCLI) {
    if let Some(t) = &args.temperature {
        let degrees = t.graden_in_celsius();
        let localized_gp = match t {
            // special case for our friends from the US
            Temperature::Fahrenheit(_) => "PCP",
            _ => "GP",
        };
        if degrees < 36.1 {
            println!("Your temperature is too low, please contact your {localized_gp}.");
            std::process::exit(1);
        } else if degrees > 37.2 {
            println!("Your temperature is too high, please contact your {localized_gp}.");
            std::process::exit(1);
        } else {
            println!("Your temperature is fine! Thanks for letting me know.");
            std::process::exit(0);
        }
    }

    if args.dangerously_ask_permissions {
        // that'll teach him
        let p: *const i32 = std::ptr::null();
        unsafe {
            println!("{}", *p);
        }
    }
}

/// Tries to guess an effective shell.
/// Effective shell is zsh on Mac and bash on Linux.
/// Effective shell is useful when $SHELL is either not set or $SHELL=ebash
fn verkrijg_effectieve_shell() -> OsString {
    let shell = env::var_os("SHELL").unwrap_or("".into());
    if shell.is_empty() || shell.as_bytes().ends_with(b"/ebash") {
        // try to guess the underlying shell based on the current OS
        let long_os_version = System::long_os_version()
            .unwrap_or("".into())
            .to_lowercase();
        // on Mac long_os_version will look like "macos 26.0.1"
        // on Linux long_os_version will look like "linux (ubuntu 24.04)"
        debug!("long_os_version = {}", long_os_version);

        if long_os_version.contains("macos") || long_os_version.contains("darwin") {
            // on Mac long_os_version will look like "macos 26.0.1"
            return which("zsh").unwrap_or(shell.into()).into();
        }

        // default to bash on other systems. It won't work on Windows. Whatever.
        return which("bash").unwrap_or(shell.into()).into();
    }
    shell
}
