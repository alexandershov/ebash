/*
ebash supports 4 modes:
1. ebash
   Regular interactive shell session
2. ebash -c "natural language command" arg0 arg1 ...
   Convert natural language command passed in `-c` argument to a shell command with LLM
   and pass arg0, arg1, ... as $0, $1, ... to the converted command.
3. ebash --just-print-command -c "natural language command" arg0 arg1 ...
   Convert natural language command passed in `-c` argument to a shell command with LLM
   and print converted command without executing it
4. ebash script.ebash arg0 arg1 ...
   Execute script.ebash and pass arg0, arg1, ... as $1, $2, ... to script
*/
